# pętla - możliwosc wykonania kodu wielokrotnie
# for - pętla iteracyjna

for i in range(5):  # od 0 do 4
    print(i)

for i in range(20):
    pass  # nic nie rób

print(i)  # 19

for _ in range(15):
    print("Test podłoga")
    # print(_)  # 14

print("Wyjscie z pętli")

for i in range(5):
    print(i * 2)
    print(i + 2)
